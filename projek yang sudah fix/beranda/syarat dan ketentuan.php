	Apa saja syarat donor darah?
Berikut adalah syarat-syarat yang harus Anda penuhi jika ingin melakukan donor darah:

1.Syarat donor darah yang paling utama adalah kondisi fisik Anda harus sehat.
2.Berusia antara 17-60 tahun. Remaja berusia 17 tahun diperbolehkan menjadi donor darah bila mendapat izin tertulis dari orangtua.
3.Memiliki berat badan minimal 45 kilogram.
4.Dalam keadaan sehat saat mendonorkan darah.
5.Suhu tubuh berkisar antara 36,6-37,5 derajat Celcius.
6.Memiliki tekanan darah pada angka 100-160 untuk sistolik dan 70-100 untuk diastolik.
7.Memiliki denyut nadi sekitar 50-100 kali per menit saat pemeriksaan.
8.Kadar hemoglobin harus minimal 12 gr/dl untuk wanita, dan minimal 12,5 gr/dl untuk pria.
Anda bisa mendonorkan darah paling banyak lima kali setahun dengan jangka waktu sekurang-kurangnya tiga bulan. Calon donor dapat mengambil dan menandatangani formulir pendaftaran, lalu menjalani pemeriksaan pendahuluan, seperti kondisi berat badan, HB, golongan darah, dan dilanjutkan dengan pemeriksaan dokter.